// 한자 정보 조회 함수
export function getKanjiInfo(character: string): KanjiInfo | undefined {
  return SAMPLE_KANJI_INFO.find((k) => k.character === character);
}

// 여러 한자 정보 조회
export function getMultipleKanjiInfo(characters: string[]): KanjiInfo[] {
  return characters
    .map((char) => getKanjiInfo(char))
    .filter((info): info is KanjiInfo => info !== undefined);
}
